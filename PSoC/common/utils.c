/***************************************************************************
 * Function: HandleError    
 *
 * Description: Disable IRQ and enter an infinite loop.
 **************************************************************************/
void HandleError(void)
{   
     /* Disable all interrupts. */
    __disable_irq();
    
    /* Infinite loop. */
    while(1u) {}
}
